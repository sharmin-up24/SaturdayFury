package com.upskill.java_5;

//Interface to Class: A class implements an interface using the implements keyword 
//and must provide implementations for all methods of the interface.

//Interface to Interface: One interface can inherit (extend) 
//another interface. The child interface inherits all methods of the parent interface.

public interface Interface {
	
	public abstract void icar();
	
	public abstract void iwheel();
	
	public abstract void idoor();
	
	public abstract void iengine();
	
	public abstract String iname(); 
	
	public abstract int iage();
	
	public abstract boolean iev();






}
